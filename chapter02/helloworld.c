#include <stdio.h>  // Includes the Standard Input/Output library.
// This library provides functions such as printf() for displaying output.

int main(void)      // Every C program starts executing in the main() function.
                    // int means the function returns an integer to the operating system.
                    // void means main() takes no arguments.
{
    printf("To C, or not to C: that is the question.\n");
    // printf() prints text to the terminal.
    // "\n" is the newline character, which moves the cursor to the next line.

    return 0;       // Indicates that the program finished successfully.
}
